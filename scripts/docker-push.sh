# ----------------------------------------------------------------------
# 📈 ОПТИМИЗАЦИЯ DOCKERFILE
# ----------------------------------------------------------------------
# Для ускорения сборки и уменьшения размера образа:
#
# 1. ✅ Используйте multi-stage builds:
#    FROM node:18 AS builder
#    COPY . .
#    RUN npm build
#    FROM node:18-alpine
#    COPY --from=builder /app/dist .
#
# 2. ✅ Оптимизируйте порядок инструкций:
#    - Сначала COPY package*.json и RUN npm install
#    - Потом COPY остальной код
#    - Это использует кэш слоёв эффективнее
#
# 3. ✅ Используйте .dockerignore:
#    node_modules
#    .git
#    *.log
#    .env
#    terraform.tfstate
#
# 4. ✅ Выбирайте минимальные базовые образы:
#    - alpine (~5 MB) вместо ubuntu (~70 MB)
#    - distroless для production (ещё меньше)