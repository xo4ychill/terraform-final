#!/bin/bash
# ======================================================================
# Скрипт сборки Docker-образа и push в Yandex Container Registry
# ======================================================================
# Назначение скрипта:
# 🔹 Автоматизация процесса сборки Docker-образа приложения
# 🔹 Push образа в Yandex Container Registry (YCR)
# 🔹 Интеграция с CI/CD-пайплайнами (GitLab CI, GitHub Actions, Jenkins)
#
# 🔹 Архитектурное место в процессе деплоя:
#    Исходный код → Сборка образа → Push в Registry → Deploy на ВМ/K8s
#
# 🔹 Требования к окружению:
#    - Docker должен быть установлен и запущен
#    - Пользователь должен иметь права на выполнение docker-команд
#    - Должна быть выполнена аутентификация в Yandex Container Registry
#    - Скрипт должен запускаться из корня проекта или с правильным путём

# ----------------------------------------------------------------------
# 🔐 НАСТРОЙКИ БЕЗОПАСНОСТИ И ОТЛАДКИ
# ----------------------------------------------------------------------
# set -euo pipefail — строгий режим выполнения bash
# 🔹 -e (errexit): Скрипт немедленно завершится с ошибкой, если любая
#    команда вернёт ненулевой код возврата. Предотвращает продолжение
#    выполнения при сбоях (например, если docker build failed).
# 🔹 -u (nounset): Скрипт завершится ошибкой при использовании
#    неопределённых переменных. Помогает отловить опечатки в именах
#    переменных на ранней стадии.
# 🔹 -o pipefail: В конвейерах (|) статус всего конвейера равен статусу
#    последней неуспешной команды. Без этого опциона ошибка в начале
#    конвейера может быть проигнорирована.
#
# ⚠️ Важно: Этот режим рекомендуется для всех production-скриптов!
#    Для отладки можно временно отключить, добавив set +e в нужном месте.
set -euo pipefail

# ----------------------------------------------------------------------
# 📋 ПАРАМЕТРЫ СКРИПТА И ПЕРЕМЕННЫЕ
# ----------------------------------------------------------------------
# REGISTRY_URL — URL контейнерного реестра Yandex Cloud
# 🔹 Формат: cr.yandex/<registry-id>
# 🔹 Пример: cr.yandex/crp1234567890abcdef
# 🔹 Получение:
#    - Через terraform output: terraform output registry_url
#    - Через YC CLI: yc container registry get <name> --format json
#    - В консоли Yandex Cloud: Container Registry → Ваш реестр → URL
#
# ⚠️ "${1:?...}" — проверка обязательности параметра
#    Если первый аргумент не передан, скрипт завершится с ошибкой
#    и выведет сообщение об использовании. Это защита от случайного
#    запуска без параметров, который привёл бы к некорректному имени образа.
REGISTRY_URL="${1:?Использование: $0 <registry_url> [image_tag]}"

# IMAGE_TAG — тег Docker-образа
# 🔹 По умолчанию: "latest" (если не указан второй аргумент)
# 🔹 Рекомендации по тегированию:
#    - ❌ Избегайте "latest" в production — используйте версии (v1.2.3)
#    - ✅ Используйте git commit hash для уникальности (git rev-parse --short HEAD)
#    - ✅ Используйте семантическое версионирование (major.minor.patch)
#    - ✅ Для CI/CD: branch-name-commit-hash (feature-login-a1b2c3d)
# 🔹 "${2:-latest}" — значение по умолчанию, если аргумент не передан
IMAGE_TAG="${2:-latest}"

# SOURCE_DIR — путь к исходному коду приложения
# 🔹 "$(dirname "$0")" — директория, где находится сам скрипт
# 🔹 "/.." — поднимаемся на уровень выше (из scripts/ в корень проекта)
# 🔹 "/src" — директория с исходным кодом и Dockerfile
# 🔹 "$(cd ... && pwd)" — получаем абсолютный путь для надёжности
#
# ⚠️ Важно: Docker build должен запускаться из директории с Dockerfile!
#    Иначе контекст сборки будет некорректным и COPY/ADD команды не сработают.
SOURCE_DIR="$(cd "$(dirname "$0")/.." && pwd)/src"

# ----------------------------------------------------------------------
# 📊 ИНФОРМАЦИОННАЯ ПАНЕЛЬ
# ----------------------------------------------------------------------
# Выводим информацию о параметрах запуска для:
# 1. Отладки — видно, с какими параметрами запустился скрипт
# 2. Аудита — в логах CI/CD остаётся история сборок
# 3. Верификации — оператор может проверить корректность перед запуском
#
# 🔹 Разделители "===" помогают визуально выделить блок в логах
# 🔹 Эмодзи улучшают читаемость в современных терминалах и CI-системах
echo "=================================================="
echo "  Сборка и пуш Docker-образа в Yandex Container Registry"
echo "=================================================="
echo "📍 Registry URL: ${REGISTRY_URL}"
echo "🏷  Image Tag:    ${IMAGE_TAG}"
echo "📁 Source Dir:   ${SOURCE_DIR}"
echo "📅 Timestamp:    $(date '+%Y-%m-%d %H:%M:%S')"
echo ""

# ----------------------------------------------------------------------
# 🔍 ПРЕДВАРИТЕЛЬНЫЕ ПРОВЕРКИ
# ----------------------------------------------------------------------
# Проверка существования директории с исходным кодом
# 🔹 Предотвращает ошибку "no such file or directory" на этапе docker build
# 🔹 Раннее завершение с понятным сообщением об ошибке
if [[ ! -d "${SOURCE_DIR}" ]]; then
    echo "❌ Ошибка: Директория с исходным кодом не найдена: ${SOURCE_DIR}"
    echo "💡 Проверьте структуру проекта и путь к директории src/"
    exit 1
fi

# Проверка существования Dockerfile
# 🔹 Docker build завершится ошибкой, если Dockerfile отсутствует
# 🔹 Проверяем заранее для более понятного сообщения об ошибке
if [[ ! -f "${SOURCE_DIR}/Dockerfile" ]]; then
    echo "❌ Ошибка: Dockerfile не найден в: ${SOURCE_DIR}/Dockerfile"
    echo "💡 Убедитесь, что Dockerfile существует в директории src/"
    exit 1
fi

# Проверка доступности Docker Daemon
# 🔹 Частая ошибка: Docker не запущен или нет прав у пользователя
# 🔹 docker info — лёгкая команда для проверки доступности демона
if ! docker info >/dev/null 2>&1; then
    echo "❌ Ошибка: Docker Daemon недоступен"
    echo "💡 Проверьте:"
    echo "   - Запущен ли Docker (systemctl status docker)"
    echo "   - Есть ли права у пользователя (группа docker)"
    echo "   - Не требуется ли sudo для docker-команд"
    exit 1
fi

echo "✅ Все предварительные проверки пройдены"
echo ""

# ======================================================================
# 📦 ШАГ 1: СБОРКА DOCKER-ОБРАЗА
# ======================================================================
# docker build — основная команда сборки образа
# 🔹 -t (tag): Присваивает имя и тег образу в формате registry/image:tag
# 🔹 . (точка): Контекст сборки — текущая директория (после cd)
#
# 🔹 Что происходит при сборке:
#    1. Docker читает Dockerfile из контекста сборки
#    2. Выполняет инструкции по порядку (FROM, RUN, COPY, EXPOSE и т.д.)
#    3. Создаёт слои образа (каждая инструкция — новый слой)
#    4. Кэширует слои для ускорения повторных сборок
#    5. Финализирует образ с указанным тегом
#
# ⚠️ Важно: 
#    - Контекст сборки (.) должен содержать все файлы, используемые в COPY/ADD
#    - .dockerignore исключает ненужные файлы (node_modules, .git, и т.д.)
#    - Сборка может занять от нескольких секунд до минут в зависимости от размера
echo "📦 [1/3] Сборка Docker-образа..."
echo "   Команда: docker build -t ${REGISTRY_URL}/app:${IMAGE_TAG} ."
echo ""

# Переход в директорию с исходным кодом перед сборкой
# 🔹 Это критически важно для корректного контекста сборки
# 🔹 Все относительные пути в Dockerfile будут относительно этой директории
cd "${SOURCE_DIR}"

# Выполнение сборки
# 🔹 Вывод docker build подробный — виден каждый шаг и кэширование
# 🔹 При ошибке скрипт завершится из-за set -e
docker build -t "${REGISTRY_URL}/app:${IMAGE_TAG}" .

# Проверка результата сборки
# 🔹 $? — код возврата последней команды (0 = успех)
# 🔹 Явная проверка для более понятного сообщения об ошибке
if [[ $? -ne 0 ]]; then
    echo "❌ Ошибка сборки Docker-образа"
    echo "💡 Возможные причины:"
    echo "   - Синтаксическая ошибка в Dockerfile"
    echo "   - Отсутствуют файлы, указанные в COPY/ADD"
    echo "   - Ошибка при установке зависимостей (npm install, pip install и т.д.)"
    echo "   - Недостаточно дискового пространства"
    exit 1
fi

echo "✅ Образ успешно собран"
echo ""

# ======================================================================
# 🔍 ШАГ 2: ПРОВЕРКА СОЗДАННОГО ОБРАЗА
# ======================================================================
# docker images — вывод информации о локальных образах
# 🔹 Фильтруем по имени собранного образа для верификации
# 🔹 Показывает: REPOSITORY, TAG, IMAGE ID, CREATED, SIZE
#
# 🔹 Зачем это нужно:
#    1. Подтверждение, что образ создан с правильным именем
#    2. Проверка размера образа (оптимизация Dockerfile)
#    3. Отладка — виден IMAGE ID для последующих операций
#
# 💡 Совет: Для production отслеживайте размер образов!
#    - Большие образы = медленный push/pull
#    - Используйте multi-stage builds для минимизации размера
#    - Регулярно очищайте старые образы: docker image prune
echo "🔍 [2/3] Проверка созданного образа..."
echo ""

# Вывод информации об образе с форматированием
# 🔹 --format — кастомный вывод для читаемости
# 🔹 Показывает только релевантную информацию
docker images "${REGISTRY_URL}/app:${IMAGE_TAG}" --format "table {{.Repository}}\t{{.Tag}}\t{{.ID}}\t{{.Size}}\t{{.CreatedAt}}"

# Дополнительная проверка — количество найденных образов
# 🔹 Если образ не найден — что-то пошло не так на этапе сборки
IMAGE_COUNT=$(docker images -q "${REGISTRY_URL}/app:${IMAGE_TAG}" | wc -l)
if [[ "${IMAGE_COUNT}" -eq 0 ]]; then
    echo "❌ Ошибка: Образ не найден в локальном реестре"
    exit 1
fi

echo ""
echo "✅ Образ найден в локальном реестре"
echo ""

# ======================================================================
# 🚀 ШАГ 3: PUSH В CONTAINER REGISTRY
# ======================================================================
# docker push — загрузка образа в удалённый реестр
# 🔹 Требует предварительной аутентификации в реестре
# 🔹 Загружает только слои, которых нет в реестре (incremental push)
# 🔹 Время загрузки зависит от размера образа и скорости интернета
#
# 🔹 Предварительные требования:
#    1. Аутентификация в Yandex Container Registry
#       yc container registry configure --id <registry-id>
#       docker login --username oauth --password $(yc iam create-token) cr.yandex
#    2. Права IAM у service account:
#       - container-registry.pusher — для загрузки образов
#       - container-registry.editor — для полного управления
#    3. Сетевой доступ к cr.yandex (не заблокирован firewall/proxy)
#
# ⚠️ Частые ошибки:
#    - "denied: requested access to the resource is denied" — нет прав IAM
#    - "unauthorized: authentication required" — не выполнен docker login
#    - "timeout" — проблемы с сетью или большой размер образа
echo "🚀 [3/3] Push в Yandex Container Registry..."
echo "   Registry: ${REGISTRY_URL}"
echo "   Image:    ${REGISTRY_URL}/app:${IMAGE_TAG}"
echo ""

# Проверка аутентификации перед push
# 🔹 docker-credential-gcr или yc credential helper должен быть настроен
# 🔹 Альтернатива: явный docker login перед запуском скрипта
if ! docker credentials cr.yandex >/dev/null 2>&1; then
    echo "⚠️  Предупреждение: Возможно, не выполнена аутентификация в реестре"
    echo "💡 Выполните команду для аутентификации:"
    echo "   yc container registry configure --id <registry-id>"
    echo "   docker login --username oauth --password \$(yc iam create-token) cr.yandex"
    echo ""
fi

# Выполнение push
# 🔹 Выводит прогресс загрузки каждого слоя
# 🔹 При ошибке сети можно перезапустить — загрузятся только недостающие слои
docker push "${REGISTRY_URL}/app:${IMAGE_TAG}"

# Проверка результата push
if [[ $? -ne 0 ]]; then
    echo "❌ Ошибка push в Container Registry"
    echo "💡 Возможные причины:"
    echo "   - Не выполнена аутентификация (docker login)"
    echo "   - Недостаточно прав IAM у service account"
    echo "   - Превышена квота хранилища реестра"
    echo "   - Проблемы с сетевым подключением"
    echo "💡 Проверьте:"
    echo "   yc container registry list"
    echo "   docker login cr.yandex"
    exit 1
fi

echo ""
echo "✅ Образ успешно отправлен в реестр"

# ======================================================================
# 📊 ИТОГОВАЯ ИНФОРМАЦИЯ
# ======================================================================
# Выводим итоговую информацию для:
# 1. Подтверждения успешного завершения
# 2. Копирования имени образа для деплоя
# 3. Аудита и логирования в CI/CD-системах
echo ""
echo "=================================================="
echo "  ✅ СБОРКА И PUSH ЗАВЕРШЕНЫ УСПЕШНО"
echo "=================================================="
echo ""
echo "📦 Полное имя образа:"
echo "   ${REGISTRY_URL}/app:${IMAGE_TAG}"
echo ""
echo "🔗 Команды для использования:"
echo "   # Pull образа на другой машине:"
echo "   docker pull ${REGISTRY_URL}/app:${IMAGE_TAG}"
echo ""
echo "   # Запуск контейнера:"
echo "   docker run -d ${REGISTRY_URL}/app:${IMAGE_TAG}"
echo ""
echo "   # Проверка в Yandex Cloud CLI:"
echo "   yc container image list --repository-name=${REGISTRY_URL#cr.yandex/}/app"
echo ""
echo "📅 Время завершения: $(date '+%Y-%m-%d %H:%M:%S')"
echo "=================================================="

# ----------------------------------------------------------------------
