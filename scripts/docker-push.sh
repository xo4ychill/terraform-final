# ----------------------------------------------------------------------
# 🐛 ОТЛАДКА И РАСПРОСТРАНЁННЫЕ ОШИБКИ
# ----------------------------------------------------------------------
# | Ошибка | Причина | Решение |
# |--------|---------|---------|
# | "permission denied" | Нет прав docker | Добавьте пользователя в группу docker |
# | "unauthorized" | Не выполнен docker login | Выполните аутентификацию в реестре |
