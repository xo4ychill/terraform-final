# 🔹 ШАГ 2: Аутентификация в Container Registry
# ----------------------------------------------------------------------
# Перед первым запуском скрипта необходимо выполнить аутентификацию:
#
# bash
# # Настройка CLI для работы с реестром
# yc container registry configure --id <registry-id>
#
# # Логин Docker в реестр Yandex Cloud
# docker login --username oauth --password $(yc iam create-token) cr.yandex
#
# 🔹 ШАГ 3: Запуск скрипта сборки
# ----------------------------------------------------------------------
# Базовый запуск (тег latest):
# bash
# ./scripts/docker-push.sh cr.yandex/crp1234567890abcdef
#
# Запуск с кастомным тегом (рекомендуется для production):
# bash
# ./scripts/docker-push.sh cr.yandex/crp1234567890abcdef v1.2.3
#
# Запуск с git-тегом (для CI/CD):
# bash
# ./scripts/docker-push.sh cr.yandex/crp1234567890abcdef $(git describe --tags)
#
