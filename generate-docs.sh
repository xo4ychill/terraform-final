#!/usr/bin/env bash

# ======================================================================
# 🚀 Terraform Registry-Level Documentation Generator
# ======================================================================
# Скрипт генерирует:
# 1. Root документацию проекта (docs/DIRECTORY_STRUCTURE.md)
# 2. Документацию каждого модуля (README.md внутри модуля)
# 3. Добавляет дерево проекта без root каталога
# ======================================================================

set -euo pipefail

# ======================================================================
# 📁 Определение путей проекта
# ======================================================================
ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
SRC_DIR="${ROOT_DIR}/src"
MODULES_DIR="${SRC_DIR}/modules"

ROOT_CONFIG="${ROOT_DIR}/.terraform-docs.yml"
MODULE_CONFIG="${ROOT_DIR}/.terraform-docs-module.yml"

DOCS_DIR="${ROOT_DIR}/docs"
ROOT_DOC="${DOCS_DIR}/DIRECTORY_STRUCTURE.md"

echo "🏆 Terraform Registry-Level Docs Generator"
echo "📁 ROOT: ${ROOT_DIR}"

# ======================================================================
# 🔍 Проверка наличия terraform-docs
# ======================================================================
if ! command -v terraform-docs >/dev/null 2>&1; then
  echo "❌ terraform-docs не установлен"
  echo "👉 Установи: brew install terraform-docs"
  exit 1
fi

echo "✅ terraform-docs: $(terraform-docs --version)"

# ======================================================================
# 📦 Проверка конфигураций
# ======================================================================
[ -f "${ROOT_CONFIG}" ] || { echo "❌ отсутствует .terraform-docs.yml"; exit 1; }
[ -f "${MODULE_CONFIG}" ] || { echo "❌ отсутствует .terraform-docs-module.yml"; exit 1; }

# ======================================================================
# 📁 Создаём директорию docs
# ======================================================================
mkdir -p "${DOCS_DIR}"

# ======================================================================
# 📄 ROOT документация (с деревом проекта)
# ======================================================================
if [ ! -f "${ROOT_DOC}" ]; then

  # ------------------------------------------------------------------
  # 🌳 Генерация дерева проекта БЕЗ root каталога
  # ------------------------------------------------------------------
  PROJECT_TREE=$(tree -a -I '.git|.terraform|node_modules|*.tfstate*|docs|.idea' src --noreport)

  # ------------------------------------------------------------------
  # 📝 создание файла root документации
  # ------------------------------------------------------------------
  cat > "${ROOT_DOC}" <<EOF
# 📦 Terraform Infrastructure

---

## 📚 Автоматически сгенерированная документация

---

## 🌳 Структура проекта (без root каталога)

\`\`\`
${PROJECT_TREE}
\`\`\`

---

## 📖 Документация модулей

<!-- BEGIN_TF_DOCS -->
<!-- END_TF_DOCS -->
EOF

fi

# ======================================================================
# ⚙️ ГЕНЕРАЦИЯ ROOT документации Terraform
# ======================================================================
echo "⚙️ Генерация ROOT документации..."

terraform-docs \
  --config "${ROOT_CONFIG}" \
  "${SRC_DIR}"

echo "✅ ROOT документация готова"

# ======================================================================
# 📦 ГЕНЕРАЦИЯ МОДУЛЕЙ
# ======================================================================
echo "⚙️ Генерация документации модулей..."

for module in "${MODULES_DIR}"/*; do

  # пропускаем не-папки
  [ -d "${module}" ] || continue

  module_name=$(basename "${module}")
  readme="${module}/README.md"

  echo ""
  echo "📦 Обрабатываем модуль: ${module_name}"

  # ------------------------------------------------------------------
  # 📝 создаём README с названием модуля (Registry-style)
  # ------------------------------------------------------------------
  cat > "${readme}" <<EOF
# Terraform Module: ${module_name}

---

## 📌 Описание

Модуль инфраструктуры: **${module_name}**

---

## 📚 Документация

<!-- BEGIN_TF_DOCS -->
<!-- END_TF_DOCS -->
EOF

  # ------------------------------------------------------------------
  # ⚙️ генерация документации terraform-docs
  # ------------------------------------------------------------------
  terraform-docs \
    --config "${MODULE_CONFIG}" \
    "${module}"

  echo "✅ ${module_name} документация обновлена"

done

# ======================================================================
# 🔍 Git diff (если проект под git)
# ======================================================================
if command -v git >/dev/null 2>&1 && git -C "${ROOT_DIR}" rev-parse --is-inside-work-tree >/dev/null 2>&1; then
  echo ""
  echo "🔍 Проверка изменений в docs..."

  git -C "${ROOT_DIR}" --no-pager diff -- "${DOCS_DIR}" || true
fi

# ======================================================================
# 🎉 Завершение работы
# ======================================================================
echo ""
echo "🏆 ГОТОВО: Terraform Registry-Level Documentation обновлена"